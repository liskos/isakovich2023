def f(n):
    a = []
    t = "0123456789abcd"
    while n > 0:
        a.append(t[n % 13])
        n = n // 13
    return ''.join(a)[::-1]

for x in range(15):
    r = int('30150', 15) + (x * 15 ** 0) + (x * 15 ** 3) + 1 * (3051 + x * 100)**2 + 2 * (3051 + x * 100) + 3 + x ** x + 1 * (103 + 10 * x)**2 + x * (103 + 10 * x) + 3 * (103 + 10 * x)**0 + 1 * (x + 1) **2 + x * (x + 1)**1 + 2 * (x+1)**0
    if r % 13 == 0:
        print(f(r))